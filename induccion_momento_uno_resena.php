    <ul id="issues">
        <li id="1930">
            <img src="linea_tiempo/images/1.png" width="256" height="256" />
            <h1>1930</h1>
            <p>
                Escuelas internacionales de origen estadounidense y canadiense comienzan a ofrecer en Colombia, cursos de capacitación y correspondencia.</p>
        </li>
        <li id="1947">
            <img src="linea_tiempo/images/2.png" width="256" height="256" />
            <h1>1947</h1>
            <p>Origen de la Educación a Distancia en Colombia, a través de la Radio Sutatenza con apoyo del sacerdote José Joaquín Salcedo.</p>
        </li>
        <li id="1967">
            <img src="linea_tiempo/images/3.png" width="256" height="256" />
            <h1>1967</h1>
            <p>Aparece el Fondo de Capacitación Popular creado por el gobierno, con el propósito de llevar educación a muchos sectores de la población, utilizando la radio, la televisión y material impreso. Desarrollando cursos de educación básica primaria para adultos, bachillerato por radio y televisión cultural.</p>
        </li>
        <li id="1970">
            <img src="linea_tiempo/images/4.png" width="256" height="256" />
            <h1>1970</h1>
            <p>El gobierno incentiva que las universidades ofrezcan programas de Educación Superior Abierta y a Distancia –ESAD-. El gobierno financia proyectos de dotación de infraestructura física, adquisición de equipos, elaboración de material didáctico y capacitación de profesores y tutores.</p>
        </li>
        <li id="1982">
            <img src="linea_tiempo/images/5.png" width="256" height="256" />
            <h1>1982</h1>
            <p>Se expide el Decreto 2412 de 1982, el cual define la –ESAD-, sus objetivos, reglamentación, dirección y procesos de inspección. Se crea el programa de educación a distancia, focalizado en una institución piloto: La Unidad Universitaria del Sur de Bogotá, UNISUR.</p>
        </li>
        <li id="1983">
            <img src="linea_tiempo/images/6.png" width="256" height="256" />
            <h1>1983</h1>
            <p>Se brinda capacitación a los docentes para el diseño de materiales didácticos en cooperación con la UNED (Universidad Nacional de Educación a Distancia) de España y se crea el Centro Nacional de Información y Documentación en Educación Abierta y a Distancia.</p>
        </li>
        <li id="1984">
            <img src="linea_tiempo/images/7.png" width="256" height="256" />
            <h1>1984</h1>
            <p>UNISUR, emprende la oferta de programas  académicos dirigida a la población del sur de Bogotá, con programas tecnológicos de la Escuela de Administración de Empresas y tecnología e Ingeniería de Alimentos, a través de la modalidad semipresencial.</p>
        </li>
        <li id="1987">
            <img src="linea_tiempo/images/8.png" width="256" height="256" />
            <h1>1987</h1>
            <p>UNISUR, crea  la Escuela de Ciencias Agrarias.</p>
        </li>
        <li id="1989">
            <img src="linea_tiempo/images/9.png" width="256" height="256" />
            <h1>1989</h1>
            <p>UNISUR, hace presencia en el departamento del Huila.</p>
        </li>
        <li id="1994">
            <img src="linea_tiempo/images/10.png" width="256" height="256" />
            <h1>1994</h1>
            <p>Se crea en la UNISUR, el programa de Tecnología en Regencia de Farmacia.</p>
        </li>
        <li id="1997">
            <img src="linea_tiempo/images/11.png" width="256" height="256" />
            <h1>1997</h1>
            <p>Se cambia la denominación de Unidad Universitaria del Sur de Bogotá (UNISUR), por la de Universidad Nacional Abierta y a Distancia (UNAD).</p>
        </li>
        <li id="1999">
            <img src="linea_tiempo/images/12.png" width="256" height="256" />
            <h1>1999</h1>
            <p>Se crea el Programa de Bachillerato Diversificado a Distancia para adultos, con el objetivo de promover la acción educativa en los diferentes niveles de formación; su proceso inicia con la alfabetización, la Educación Básica y Media, la Educación para el Trabajo y Desarrollo Humano, la Educación Continuada y la Educación Superior hasta la Posgradual.</p>
        </li>
        <li id="2004">
            <img src="linea_tiempo/images/13.png" width="256" height="256" />
            <h1>2004</h1>
            <p>Se obtiene la primera licencia de funcionamiento de la UNAD Florida (Estados Unidos), ofreciendo en la actualidad cinco (5) programas de Pregrado, cuatro (4) programas de Maestría y tres (3) Doctorados.</p>
        </li>
        <li id="2005">
            <img src="linea_tiempo/images/14.png" width="256" height="256" />
            <h1>2005</h1>
            <p>La UNAD obtiene mediante Resolución 6215 del 22 de Diciembre de 2005, el carácter académico de Universidad. Se crea el Sistema Nacional de Consejería, para el seguimiento y acompañamiento de los estudiantes nuevos.</p>
        </li>
        <li id="2006">
            <img src="linea_tiempo/images/15.png" width="256" height="256" />
            <h1>2006</h1>
            <p>La UNAD, logra la condición como ente Universitario Autónomo del Orden Nacional, lo que implica independencia a nivel jurídico, autonomía académica, administrativa y financiera, patrimonio independiente, capacidad para gobernarse y vinculada al Ministerio de Educación Nacional.</p>
        </li>
        <li id="2007">
            <img src="linea_tiempo/images/16.png" width="256" height="256" />
            <h1>2007</h1>
            <p>Se virtualizan los primeros cursos de la UNAD, lo que significan el cambio en la metodología de estudio, mediante el uso de la plataforma moodle, con el estándar CORE.</p>
        </li>
        <li id="2012">
            <img src="linea_tiempo/images/17.png" width="256" height="256" />
            <h1>2012</h1>
            <p>Mediante Resolución 3988 del 18 de Abril de 2012, la UNAD recibe Alta Acreditación por parte del Ministerios de Educación Nacional para cinco (5) programas: Comunicación Social, Licenciatura en Etnoeducación, Zootecnia, Ingeniería de Sistemas y Administración de Empresas.</p>
            <p>Se crea la Vicerrectoría de Servicios a Aspirantes, Estudiantes y Egresados – VISAE. El ministerio de la TIC en alianza con la UNAD, implementan los programas de ciudadano y empresario digital como estrategia de alfabetización digital nacional.</p>
        </li>
        <li id="2013">
            <img src="linea_tiempo/images/18.png" width="256" height="256" />
            <h1>2013</h1>
            <p>La Comisión Nacional de Acreditación CNA, avala el inicio del proceso de acreditación institucional</p>
        </li>
    </ul>
